export default function ajax_x(type, path, data = "", callback = "", data_for_callback = "") {
	function getCookie(name) {
        let matches = document.cookie.match(new RegExp(
          "(?:^|; )" + name.replace(/([\.$?*|{}\(\)\[\]\\\/\+^])/g, '\\$1') + "=([^;]*)"
        ));
        return matches ? decodeURIComponent(matches[1]) : undefined;
    }
	switch (type) {
		case "POST":
		{
			let formData = new FormData();
			for (let key of Object.keys(data)) {
				formData.append(key, data[key]);
			} 
			let xhr = new XMLHttpRequest();
			xhr.open("POST", path);
			xhr.setRequestHeader("X-XSRF-TOKEN", getCookie("XSRF-TOKEN"));
			xhr.send(formData);
			xhr.onload = function() {
				if (callback) {
					if (data_for_callback) {
						callback(xhr, data_for_callback);
					} else {
						callback(xhr);
					}                
				}
			}
			break;
		}
		case "GET":
		{
			let xhr = new XMLHttpRequest();

			if (data) {
				path = path + "/" + data;
			}

			xhr.open("GET", path);
			xhr.setRequestHeader("X-XSRF-TOKEN", getCookie("XSRF-TOKEN"));
			xhr.send();
			
			if (callback) {
				xhr.onload = function() {
					callback(xhr);                
				}
			}
			break;
		}

		case "DELETE":
		{
			path = path ? path + "/" + data : path;
			let xhr = new XMLHttpRequest();
			xhr.open("DELETE", path);
			xhr.setRequestHeader("X-XSRF-TOKEN", getCookie("XSRF-TOKEN"));
			xhr.send();
			xhr.onload = function() {
				if (callback) {
					callback(xhr);
				}
			}
			break;
		}
	}        
}